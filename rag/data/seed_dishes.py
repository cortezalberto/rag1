from typing import Any


def _dish_template(
    name: str,
    category: str,
    price_cents: int,
    tags: list[str],
    description_simple: str,
    ingredients: list[str],
    allergens_contains: list[str],
    allergens_may_contain: list[str],
    spicy_level: str,
    gluten_free: str,
    vegan: str,
    vegetarian: str,
    portion: str,
    adaptations: list[str],
    notes: str,
    pairing: str,
) -> dict[str, Any]:
    """Build a dish data dictionary with ficha text."""
    ficha = (
        f"FICHA TÉCNICA (fuente de verdad)\n"
        f"Plato: {name}\n"
        f"Categoría: {category}\n"
        f"Descripción simple: {description_simple}\n\n"
        f"Ingredientes principales: {', '.join(ingredients)}\n\n"
        f"Alérgenos (contiene): {', '.join(allergens_contains) if allergens_contains else 'No especificado'}\n"
        f"Alérgenos (puede contener / trazas): {', '.join(allergens_may_contain) if allergens_may_contain else 'No especificado'}\n\n"
        f"Picante: {spicy_level}\n"
        f"Sin TACC / gluten: {gluten_free}\n"
        f"Apto vegano: {vegan}\n"
        f"Apto vegetariano: {vegetarian}\n\n"
        f"Porción: {portion}\n"
        f"Adaptaciones posibles: {'; '.join(adaptations)}\n\n"
        f"Notas del chef: {notes}\n"
        f"Maridaje sugerido: {pairing}\n"
    )
    return {
        "name": name,
        "category": category,
        "price_cents": price_cents,
        "tags": tags,
        "ficha_text": ficha,
    }


